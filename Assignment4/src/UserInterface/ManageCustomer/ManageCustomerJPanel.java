        private void populateTable(){
        
        DefaultTableModel dtm = (DefaultTableModel) tblCustomer.getModel();
        dtm.setRowCount(0);
        for(Customer customer: customerDirectory.getCustomerList()){
            Object[] row = new Object[1];
            row[0] = customer;   
            dtm.addRow(row);    
        }
        }
    
    

