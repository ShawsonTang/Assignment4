    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblFlight.getSelectedRow();
        if(selectedRow >=0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null,"Would your like to delete this infomation?","Warning",JOptionPane.WARNING_MESSAGE);
            if(dialogResult == JOptionPane.YES_NO_OPTION){
                Flight flight = (Flight) tblFlight.getValueAt(selectedRow,0);
                if(!travelAgency.getTravelOffice().getCustomerDirectory().getCustomerList().isEmpty()){
                    for(Customer c : travelAgency.getTravelOffice().getCustomerDirectory().getCustomerList()){
                      c.deleteFlight(flight);
                    }
                }
                flightSchedule.deleteFlight(flight);
                if(flightSchedule.getFlightList().isEmpty()){
                      travelAgency.getMasterFlightSchedule().getFlightScheduleList().remove(flightSchedule);
                }
                populateTable();
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Please select a row from table first.","Warning",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


