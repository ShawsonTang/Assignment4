    private void populateTable(){
        
        DefaultTableModel dtm = (DefaultTableModel) tblFlight.getModel();
        dtm.setRowCount(0);
        if(flightSchedule!=null){
        for(Flight flight: flightSchedule.getFlightList()){
            Object[] row = new Object[dtm.getColumnCount()];
            row[0] = flight;   
            row[1] = flight.getAirplane();
            row[2] = flight.getFrom();
            row[3] = flight.getTo();
            Date date = flight.getDepartureTime();  
            DateFormat dateFormat = new SimpleDateFormat("dd-MMM-yyyy HH:mm:ss");  
            String strDate = dateFormat.format(date);
            row[4] = strDate;
            Date date2 = flight.getArrivalTime();  
            DateFormat dateFormat2 = new SimpleDateFormat("dd-MMM-yyyy HH:mm:ss");  
            String strDate2 = dateFormat2.format(date2);
            row[5] = strDate2;
            row[6] = flight.getTimeSlot();
            dtm.addRow(row);    
        }
        }
    }
