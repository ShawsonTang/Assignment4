    private void btnViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailsActionPerformed
        // TODO add your handling code here:
         int selectedRow = tblCustomer .getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select a row of the first table to view details.","Warning",JOptionPane.WARNING_MESSAGE);
        }else{
            Customer customer = (Customer) tblCustomer.getValueAt(selectedRow,0);
            ViewCustomerJPanel panel = new ViewCustomerJPanel(rightPanel,customer);
            rightPanel.add("ViewCustomerJPanel",panel);
            CardLayout layout = (CardLayout) rightPanel.getLayout();
            layout.next(rightPanel);        
    }//GEN-LAST:event_btnViewDetailsActionPerformed
    }
